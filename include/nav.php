<script>
  if (window.feather) { feather.replace(); }

  // State for deduping
  let lastAlertItems = [];

  // Escape HTML for safe rendering
  function escapeHtml(str) {
    return String(str)
      .replace(/&/g, '&amp;')
      .replace(/</g, '&lt;')
      .replace(/>/g, '&gt;')
      .replace(/"/g, '&quot;')
      .replace(/'/g, '&#039;');
  }

  function sameItems(a, b) {
    if (!Array.isArray(a) || !Array.isArray(b)) return false;
    if (a.length !== b.length) return false;
    const sa = [...a].sort().join('|');
    const sb = [...b].sort().join('|');
    return sa === sb;
  }

  function renderNotifications(items) {
    const list = document.getElementById('notifList');
    const badge = document.getElementById('notifBadge');

    list.innerHTML = '';

    if (!items || items.length === 0) {
      list.innerHTML = `
        <a href="#" class="dropdown-item">
          <span class="dropdown-item-icon bg-success text-white">
            <i class="fas fa-check"></i>
          </span>
          <span class="dropdown-item-desc">All good. No low-stock items.</span>
        </a>`;
      badge.style.display = 'none';
      badge.textContent = '0';
      if (window.feather) { feather.replace(); }
      return;
    }

    const frag = document.createDocumentFragment();
    items.forEach(name => {
      const a = document.createElement('a');
      a.href = '#';
      a.className = 'dropdown-item dropdown-item-unread';
      a.innerHTML = `
        <span class="dropdown-item-icon bg-danger text-white">
          <i class="fas fa-exclamation-triangle"></i>
        </span>
        <span class="dropdown-item-desc">
          Low stock: <b>${escapeHtml(name)}</b>
          <span class="time">Just now</span>
        </span>`;
      frag.appendChild(a);
    });
    list.appendChild(frag);

    badge.textContent = items.length;
    badge.style.display = 'inline-block';
    if (window.feather) { feather.replace(); }
  }

  function checkLowStock() {
    fetch('data/check_stock.php', { headers: { 'Accept': 'application/json' } })
      .then(res => res.json())
      .then(items => {
        if (!Array.isArray(items)) items = [];
        // Only re-render if changed
        if (!sameItems(items, lastAlertItems)) {
          renderNotifications(items);
          lastAlertItems = items;
        }
      })
      .catch(err => {
        console.error('Stock check error:', err);
      });
  }

  // Mark all as read
  document.addEventListener('click', function(e) {
    const mark = document.getElementById('markAllRead');
    if (mark && e.target === mark) {
      e.preventDefault();
      lastAlertItems = [];
      renderNotifications([]);
    }
  });

  // Initial load and poll every 30s
  checkLowStock();
  setInterval(checkLowStock, 30000);
</script>